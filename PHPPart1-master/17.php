// Operator aritmatika adalah operator matematika yg digunakan
// untuk pengolahan bilangan. contoh nya seperti, pertambahan, 
// pengurangan, perkalian, pembagian, modulus, plus dan minus.
// Berikut adalah contoh nya
$a = 100;
$b = 10;

// contoh pertambahan
echo "contoh pertambahan : ";
echo "<br/>";
echo $a+$b;
echo "<br/>";
echo "<br/>";

// contoh pembagian
echo "contoh pembagian :";
echo "<br/>";
echo $a/$b;
echo "<br/>";
echo "<br/>";

// contoh pengurangan 
echo "contoh pengurangan :";
echo "<br/>";
echo $a-$b;
echo "<br/>";
echo "<br/>";

// contoh perkalian
echo "contoh perkalian :";
echo "<br/>";
echo $a*$b;
echo "<br/>";
echo "<br/>";

//contoh plus dan minus
echo "contoh plus dan minus :";
echo "<br/>";
echo $b-$a;
echo "<br/>";
echo $a+$b;
echo "<br/>";
echo "<br/>";

// contoh modulus
echo "contoh modulus :";
echo "<br/>";
echo $b%$a;

?>